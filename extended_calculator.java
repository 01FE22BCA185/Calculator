    float  square(float side){
        return (side*side);
     }
     
     float  cube(float side){
        return (side*side*side);
     }
     float Modulus(float a,float b){
        return (a%b);
     }
     double areaoftria(double base,double height){
        return (0.5*base*height);
     }
